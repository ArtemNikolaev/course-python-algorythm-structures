# Выполнить логические побитовые операции «И», «ИЛИ» и др. над числами 5 и 6.
# Выполнить над числом 5 побитовый сдвиг
# вправо и влево на два знака.

print('5: побитовый сдвиг влево: ', 5 << 2)
print('5: побитовый сдвиг вправо: ', 5 >> 2)

print('6: побитовый сдвиг влево: ', 6 << 2)
print('6: побитовый сдвиг вправо: ', 6 >> 2)
